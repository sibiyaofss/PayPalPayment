
--DROP TABLE TRANSACTION;
CREATE TABLE TRANSACTION (
TNXAMOUNT NUMBER(17,5) NOT NULL,
BENEFICIARYACCOUNT VARCHAR2(200) NOT NULL,
BENEFICIARYNAME VARCHAR2(200),
BENEFICIARYID VARCHAR2(1000),
CURRENCY VARCHAR2(50),
ID INT(50),
PAYMENTMETHOD VARCHAR2(100),
TRANSACTIONDATETIME VARCHAR2(200),
INVOICENUMBER VARCHAR2(100),
PAYMENTMODE VARCHAR2(100),
APPLICANTID VARCHAR2(100),
APPLICANTNAME VARCHAR2(100),
APPLICANTACCOUNT VARCHAR2(100),
APPLICANTADDRESS CLOB,
TRANSACTION_TYPE VARCHAR2(100),
PRIMARY KEY (ID)
);

